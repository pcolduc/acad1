package Day17;

public class Example01 {
    /*
    * 컬렉션 프레임워크
    * - 자료구조는 프로그램 실행 시 메모리에 자료를 유지하고 관리하기 위해 사용.
    * - 배열은 정한 크기를 변경하거나 삭제를 할 수 없음.
    * 그래서 자바는 배열의 한계점을 극복하기 위해 필요한 자료구조를 java.util 패키지에서 제공하고 있음.
    *
    * 즉) 컬렉션 프레임워크란 기존에 존재했던 자료구조에
    * 인터페이스로 설께된 기능을 클래스를 통해 제공하여 데이터 관리에 용이한 자료구조객체를 구조화한 것.
    *
    * 인터페이스 : List, Set, Map
    * List : 데이터 중복을 허용, 순서가 있는 데이터 집합
    *       구현클래스(ArrayList, LinkedList)
    *
    * set : 데이터 중복 허용안함.
    *       구현클래스(HashSet, LinkedList)
    * Map :
    * 키(Key), 값(Value) 의 쌍으로 이루어진 데이터 집합
    * 순서없고, 키 중복 불가능 , 값은 중복가능
    *       구현 클래스(HashMap, HashTable, LinkedHashMap)*/
}
